
/* blur refresh */

function blurAndRefresh() {
    // Add the blur class to the body element
    document.body.classList.add('blur');
    
    // Wait for 1 seconds
    setTimeout(function() {
      // Refresh the page
      location.reload();
    }, 2000);
  }


